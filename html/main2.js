document.addEventListener("DOMContentLoaded", function() {
    
    // Obtén el enlace de "Crisis Políticas"
    var crisisPoliticas = document.getElementById('crisisPoliticas');

    // Agrega un evento de clic al enlace de "Crisis Políticas"
    crisisPoliticas.addEventListener('click', function(event) {
        // Previene el comportamiento por defecto del enlace
        event.preventDefault();
        
        // Redirige a la página 2 y a la sección deseada
        window.location.href = '/index.html#crisis-politicas';
    });
});


